
public class CartellaClinicaModel {
    private String specie;
    private int larghezza;
    private int lunghezza;
    private int peso;
    private String luogo;
    private String data;




    public String getSpecie() {
        return specie;
    }

    public int getLarghezza() {
        return larghezza;
    }

    public int getLunghezza() {
        return lunghezza;
    }

    public int getPeso() {
        return peso;
    }

    public String getLuogo() {
        return luogo;
    }

    public String getData() {
        return data;
    }




    public void setSpecie(String n) {
        specie = n;
    }

    public void setLarghezza(int n) {
        larghezza = n;
    }

    public void setLunghezza(int n) {
        lunghezza = n;
    }

    public void setPeso(int n) {
        peso = n;
    }

    public void setLuogo(String n) {
        luogo = n;
    }

    public void setData(String n) {
        data = n;
    }
    


}